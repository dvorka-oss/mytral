# MyTraL upstream tarball builder for Linux distribution maintainers.
#
# Creates a .tar.gz archive containing only the files needed to build,
# test, and package MyTraL — no development artifacts, no runtime data.
#
# Usage:
#   ./tarball-build.sh              # build tarball from the repo root
#   ./tarball-build.sh /path/to/repo  # build from a specific repo path
#
# Output: distro/mytral-<version>.tar.gz

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="${1:-$(cd "${SCRIPT_DIR}/../.." && pwd)}"

# ---------------------------------------------------------------------------
# Resolve version from mytral/version.py
# ---------------------------------------------------------------------------
VERSION=$(cd "${REPO_ROOT}" && uv run python -c "
import sys; sys.path.insert(0, 'mytral')
import version; print(version.__version__)
" 2>/dev/null || echo "unknown")

echo "=== MyTraL tarball builder ==="
echo "  Version:  ${VERSION}"
echo "  Repo:     ${REPO_ROOT}"

# ---------------------------------------------------------------------------
# Build directory setup
# ---------------------------------------------------------------------------
# Use a temp directory outside the repo to avoid "cp: cannot copy a directory
# into itself" when copying build/ into the tarball source tree.
BUILD_DIR="/tmp/mytral-tarball-build"
DISTRO_DIR="${REPO_ROOT}/distro"
TARBALL_NAME="mytral-${VERSION}.tar.gz"
TARBALL_PATH="${DISTRO_DIR}/${TARBALL_NAME}"

echo "  Build dir: ${BUILD_DIR}"
echo "  Tarball:   ${TARBALL_PATH}"

# Clean previous build
rm -rf "${BUILD_DIR}"
rm -rf "${DISTRO_DIR}"
mkdir -p "${BUILD_DIR}/mytral-${VERSION}"
mkdir -p "${DISTRO_DIR}"

# ---------------------------------------------------------------------------
# Clean repo of stale build artifacts before copying
# ---------------------------------------------------------------------------
echo ""
echo "--- Cleaning repo of stale build artifacts ---"
rm -rf "${REPO_ROOT}/build/tarball/build"

# ---------------------------------------------------------------------------
# Copy project source to build directory
# ---------------------------------------------------------------------------
echo ""
echo "--- Copying project source ---"
SRC="${BUILD_DIR}/mytral-${VERSION}"

# Directories to copy (production source + build infrastructure)
cp -r "${REPO_ROOT}/mytral"       "${SRC}/"
cp -r "${REPO_ROOT}/tests"        "${SRC}/"
cp -r "${REPO_ROOT}/build"        "${SRC}/"
cp -r "${REPO_ROOT}/make"         "${SRC}/"
cp -r "${REPO_ROOT}/docs"         "${SRC}/"
cp -r "${REPO_ROOT}/licenses"     "${SRC}/"

# Top-level files
cp "${REPO_ROOT}/pyproject.toml"  "${SRC}/"
cp "${REPO_ROOT}/Makefile"        "${SRC}/"
cp "${REPO_ROOT}/README.md"       "${SRC}/"
cp "${REPO_ROOT}/LICENSE"         "${SRC}/"
cp "${REPO_ROOT}/CHANGELOG.md"    "${SRC}/"
cp "${REPO_ROOT}/CREDITS.md"      "${SRC}/"
cp "${REPO_ROOT}/CONTRIBUTE.md"   "${SRC}/"
cp "${REPO_ROOT}/KNOWN_ISSUES.md" "${SRC}/"
cp "${REPO_ROOT}/.gitignore"      "${SRC}/"

# ---------------------------------------------------------------------------
# Prune development artifacts
# ---------------------------------------------------------------------------
echo ""
echo "--- Pruning development artifacts ---"

cd "${SRC}"

# Remove Python cache directories
find . -type d -name "__pycache__" -exec rm -rf {} + 2>/dev/null || true
find . -type d -name "*.egg-info" -exec rm -rf {} + 2>/dev/null || true

# Remove lint/test caches
rm -rf .ruff_cache .pytest_cache

# Remove IDE directories
rm -rf .idea .vscode .claude

# Remove virtual environment (if any slipped in)
rm -rf .venv venv

# Remove uv lock file (maintainer generates their own)
rm -f uv.lock

# Remove vibe coding directory (not needed for packaging)
rm -rf vibe

# Remove generated HTML documentation (source Markdown is in docs/)
rm -rf mytral/static/documentation

# Remove stale tarball build artifacts (from prior runs)
rm -rf build/tarball/build

# Remove desktop build spec files (generated by PyInstaller)
rm -f build/desktop/*.spec 2>/dev/null || true

# Remove macOS metadata files
find . -name ".DS_Store" -delete 2>/dev/null || true

# Remove editor backup files
find . -type f \( -name "*~" -o -name "*.swp" -o -name "*.swo" \) -delete 2>/dev/null || true

# Remove empty directories that may remain after pruning
find . -type d -empty -delete 2>/dev/null || true

# ---------------------------------------------------------------------------
# Create tarball
# ---------------------------------------------------------------------------
echo ""
echo "--- Creating tarball ---"
cd "${BUILD_DIR}"
tar czf "${TARBALL_PATH}" "mytral-${VERSION}"

# ---------------------------------------------------------------------------
# Report
# ---------------------------------------------------------------------------
echo ""
echo "================================================================================"
echo "  TARBALL CREATED"
echo "================================================================================"
echo "  Location: ${TARBALL_PATH}"
echo "  Size:     $(du -h "${TARBALL_PATH}" | cut -f1)"
echo "  Files:    $(tar tzf "${TARBALL_PATH}" 2>/dev/null | wc -l) entries"
echo ""
echo "  To inspect:  tar tzf ${TARBALL_PATH} | less"
echo "  To extract:  tar xzf ${TARBALL_PATH} -C /tmp"
echo "================================================================================"

# Clean up build directory
rm -rf "${BUILD_DIR}"
